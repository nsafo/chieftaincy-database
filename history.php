      <p>The place of mythology in the historiography of a people is debated but not discarded even by 
	 hardcore scientific historians. The point here is that beneath the embellishment, origin stories 
	 and other cultural explanatory theories hold truths necessary to a good reconstruction of what 
	 may have happened thousands of years ago.</p>

<p>In the case of the Fante of Ghana, how they came to live where they presently are is a fascinating 
story with all the makings of a good Hollywood epic.</p>

<p>The Fante are the coast-dwelling people who live between the western border of Ghana’s capital city 
Accra and Cape Three Points, the southernmost point of the country located in the Western Region. 
The Fante are a part of the Akan people, the largest ethnic group in Ghana who make up more than 
50% of the country’s population. The Akan comprise more than 20 sub-groups among which are the Asante, 
undoubtedly the most-known people in Ghana to people outside.</p>

<p>Mankessim, which translates into “in the big town”, is the traditional headquarter-town of the Fante. 
It was the first major town they founded but until the 13th century, that land did not belong to the Fante.</p>

<p>Oral historians in the courthouses of Fante chieftains recount that their forefathers tore away from 
the Bono people, another Akan group in Ghana’s hinterlands, in about the 1200s. Indeed, Fante means 
“half/part torn” and further credence is lent to the tearing away from the Bono by the fact that there 
is a very close proximity in the language of the Bono and that of the Fante. Descending southwards, the 
Fante were split into three groups and led by three mysterious warrior-priests, namely Oburumankoma, 
Odapagyan and Oson. The latter warrior-priest conquered the autochthonous Etsii people and founded Mankessim.</p>

<p>Casely B. Essamuah summarizes the leadership of the trio:</p>

<p>“These were all under the leadership of three legendary leaders: Oburumankoma (whale), Odapagyan (eagle),
 and Oson (elephant)—patriarchs and priests who, in addition to their magical regalia, also possessed mfoa 
 (short swords) signifying their judicial authority. They also represented the three-tiered system of the
 natural order and the mastery that each animal is said to have had over its sphere.”</p>

<p>Although, the Fante believe these three men were literal beings with magical powers, there is a school of 
thought that argues that the trio are personified conceptions of beliefs. The fact that they are not known 
by any names but names of animals thought to dominate spheres of the natural environment boosts this school of thought.</p>

<p>For proponents of this argument, including F.L. Bartels writer of The Roots of Ghana Methodism, Oburumankoma, 
Odapagyan and Oson are supposed to be metaphors of Fante self-perception and a privileged retelling of the 
story of their founding in their present home.</p>

<p>Oburumankoma the whale is supposed to portray how the Fante were brave fisher-folks; Odapagyan the eagle speaks 
of Fante aerial ability and Oson the elephant symbolizes Fante land dominance.</p>

<p>Further support for this school of thought comes from geo-historical analyses. The hinterlands from where the Fante
 tore away from the Bono, has no sea hence the reality of Oburumankoma the whale, at least at that time, would seem implausible.</p>

<p>In spite of this, the people believe that their three warrior-priests continue to watch over them from the spirit world.</p>

<p>Essamuah adds:</p>

<p>“The place of interment of these three leaders, in the thicket of trees some ten miles from the city of Mankessim, 
became in time the “habitat of ghosts [asamanpow] or of spiritual powers inhering in nature [abosompow]”…In addition 
to serving as the spiritual watchdog over the interests of the Fante community, the Nananom Mpow became the mnemonic 
of the ancient Fante’s historical identity. It was here in the grove that histories of the people and especially of royal families were recounted.”</p>
